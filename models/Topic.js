// models/Topic.js
import mongoose from 'mongoose';

const { Schema, model } = mongoose;

const topicSchema = new Schema({
    title: {
        type: String,
        required: true
    },
    content: {
        type: String,
        required: true
    },
    user: {
        type: Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    }
});

export default model('Topic', topicSchema);
